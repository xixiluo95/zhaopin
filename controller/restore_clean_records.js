async function main() {
  initFeishuClient();
  const target = listTargets().find(function(t) { return t.isDefault; }).name;
  const page = await listRecords(target, { pageSize: 500 });
  let restored = 0;

  for (const item of page.items) {
    const name = (item.fields['公司名称'] || '').trim();
    const currentDesc = (item.fields['公司简介'] || '').trim();

    for (const [key, patch] of Object.entries(RESTORE_MAP)) {
      if (name.indexOf(key) >= 0 && !currentDesc) {
        try {
          await updateRecord(target, item.record_id, patch);
          restored++;
          console.log('[RESTORED] ' + name + ' | ' + (patch['公司简介'] || '').slice(0, 60));
        } catch (e) {
          console.log('[FAIL] ' + name + ': ' + e.message);
        }
        break;
      }
    }
  }

  console.log('\nTotal restored: ' + restored);
}

main().catch(function(error) {
  console.error('[RestoreClean] Failed: ' + error.message);
  process.exitCode = 1;
});
